                                <p>Translated as the Sky Dwellers, the Aredani are a racial branch of Spriggans broken off from the early ancestors that migrated south into the southeastern lands. They have chosen to evolved towards a high altitude lifestyle, and have a large feathered wingspan of beautiful colorations. The Aredani live within the land of Greenspire. This group has primarily been named the Skywing by human travelers. Height: 3’5-3’8” Thin, long pointed ears. More human than Elven in appearance. Expansive wingspan, with a myriad of silver, white, violet, or brown feathers. Fair to mildly darkened skin.</p>
