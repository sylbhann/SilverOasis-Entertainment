                        <h3>What is a Spriggan?</h3>
                        <ul>
                            <li>
                            <p>Spriggan, which primarly inhabit the surrounding 
                                lands of Anubhain and Greenspire, however 
                                travelers and companions can be found throughout 
                                all of southern Elysvail.</p>
                            </li>
                            <li>
                                <p>Concealed by the vast tall and thick canopies of the trees, the Enari maintain a rather prosperous existence among nature and wildlife. The Enari have takes to using the hunting lands of the territorial Sage wolves for protection against foreign enemies and have tamed few as loyal guardians of their domain. The Sage wolves are native to the southern forests and offer a natural form of protection from any form of unwanted danger that may enter the wilderness from beyond.</p>
                            </li>
                            <li>
                                <p>Aside from the wolves, they often have various forms of birds as outside pets within their houses among the trees. Outside their homeland, they often ask for the assistance of the Elysians as traveling companions if some wish to depart for the human lands west of the mountains. Being that they are rather small in size, even their wolf companions may not be able to protect them from every dangerous encounter.</p>
                            </li>
                            <li>
                                <p>The Args and other races see Spriggans as a delicacy, and see no hesitation in ambushing them on the roads in the open country. The Spriggans are very intelligent humanoids, often in trade with the elves for their inventions and creations within their forest dwellings. They are always tinkering with nature and have a knack for creating new kinds of plant life as well as new forms of wooden material. A variety of the Spriggan’s small domed houses show testament to this, as their homes among the trees are what would be called a wood crafter’s dream, or rather a wooden sphere floating among the trees. Among the ground level there can often be found homes for the wolves that live below them. </p>
                            </li>
                            <li>
                                <p>They have discovered that the more they lure the creatures toward their cities and homes and feed them at certain intervals, over time the wolves became easier to tame. After some time they were able to only slightly domesticate them, but only to a point. They still retain their feral ways, but are more protective of their Spriggan keepers.</p>
                            </li>
                            <li>
                                <p>They are rather small beings. Their average height being around 3’5” to 3’8” in standing height. They are athletic in build, similar to the look of a tiny human. They are usually of fair skin, with light to dark red or brown hair. They have a variation of blue, green, or violet eyes. They have small pointed ears, and the women often wear a decoration of feathers in certain bodily arrangements. They are rather merry folk, but serious in tone of life.</p>
